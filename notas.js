const nota = 10

if (nota === 10){
    console.log('Aprovado no Teste')
}else if (nota >=7 && nota<10){
    console.log('Aprovado')    
}else if (nota >=5 && nota<7){
    console.log('Recuperação')        
}else if (nota >=0 && nota<5){
    console.log('Reprovado')    
}else{
    console.log('error')  
}

